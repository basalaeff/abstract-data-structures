#include "b.SinglyLinkedList.hpp"

// ============================================================================
// B3 Удаление элемента (из головы): BDELHEAD
// ============================================================================
// 1) Если список пуст — выбрасывается исключение.
// 2) Голова списка перемещается на следующий элемент.
// 3) Освобождается память удаляемого узла.
// 4) Уменьшаем размер списка.
// Сложность: O(1) - удаление из головы односвязного списка выполняется за
// константное время.

void SinglyLinkedList::removeFromHead() {
  if (head_ == nullptr) {
    throw std::runtime_error("Cannot remove head: list is empty");
  }
  Node* temp = head_;
  head_ = head_->next_;
  delete temp;
  size_--;
}