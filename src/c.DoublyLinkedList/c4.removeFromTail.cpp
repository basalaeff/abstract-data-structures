#include "c.DoublyLinkedList.hpp"

// ============================================================================
// C4 Удаление элемента из хвоста: CDELTAIL
// ============================================================================
// 1) Если список пуст, функция завершается.
// 2) Сохраняется указатель на удаляемый узел (tail_).
// 3) tail_ перемещается на предыдущий элемент (tail_->prev_).
// 4) Если после удаления список не стал пустым — у нового хвоста next_
// устанавливается в nullptr.
// 5) Если список стал пустым — head_ также устанавливается в nullptr.
// 6) Освобождается память удалённого узла.
// 7) Уменьшается размер списка.
// Сложность: O(1) — выполняется за константное время.

void DoublyLinkedList::removeFromTail() {
  if (tail_ == nullptr) {
    return;
  }
  DoublyNode* temp = tail_;
  tail_ = tail_->prev_;
  if (tail_ != nullptr) {
    tail_->next_ = nullptr;
  } else {
    head_ = nullptr;  // список стал пустым
  }
  delete temp;
  size_--;
}