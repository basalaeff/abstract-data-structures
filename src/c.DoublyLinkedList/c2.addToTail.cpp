#include "c.DoublyLinkedList.hpp"

// ============================================================================
// C2 Добавление элемента в хвост: CADDTAIL value
// ============================================================================
// 1) Создаётся новый узел.
// 2) Если список пуст, новый узел становится
//    одновременно головой и хвостом списка.
// 3) Если список не пуст:
//    - prev_ нового узла указывает на текущий хвост;
//    - next_ текущего хвоста указывает на новый узел;
//    - tail_ обновляется и теперь указывает на новый узел.
// 4) Увеличивается размер списка.
//
// Сложность: O(1) — выполняется за константное время.

void DoublyLinkedList::addToTail(const std::string& value) {
  DoublyNode* newNode = new DoublyNode(value);
  if (tail_ == nullptr) {
    head_ = tail_ = newNode;
  } else {
    newNode->prev_ = tail_;
    tail_->next_ = newNode;
    tail_ = newNode;
  }
  size_++;
}