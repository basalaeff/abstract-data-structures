#include "d.Stack.hpp"

// ============================================================================
// D1 Добавление элемента: DPUSH value
// ============================================================================
// 1) Создаётся новый узел.
// 2) next_ нового узла устанавливается на текущий top_ (вершину стека).
// 3) top_ обновляется и теперь указывает на новый узел.
// 4) Увеличивается размер стека.
// Стек работает по принципу LIFO (Last In, First Out).
// Сложность: O(1)

void Stack::push(const std::string& value) {
  Node* newNode = new Node(value);
  newNode->next_ = top_;
  top_ = newNode;
  size_++;
}